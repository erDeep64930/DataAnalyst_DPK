#Program for Demonstrating Arithmetic Opereators
#ArithOperatorsEx1.py
a=float(input("Enter Value of a:"))
b=float(input("Enter Value of b:"))
#Apply Arithmetic Operators
print("********************************************")
print("Results of Arithmetic Opereators")
print("********************************************")
print("\t\tSum({},{})={}".format(a,b,a+b))
print("\t\tSub({},{})={}".format(a,b,a-b))
print("\t\tMul({},{})={}".format(a,b,a*b))
print("\t\tDiv({},{})={}".format(a,b,a/b))
print("\t\tFllorDiv({},{})={}".format(a,b,a//b))
print("\t\tModDiv({},{})={}".format(a,b,a%b))
print("\t\tPower({},{})={}".format(a,b,a**b))
print("********************************************")