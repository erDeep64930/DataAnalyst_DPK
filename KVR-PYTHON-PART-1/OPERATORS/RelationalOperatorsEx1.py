#Program for Demonstrating Relational Operators
#RelationalOperatorsEx1.py
a,b=int(input("Enter Value of a:")),int(input("Enter Value of b:")) # Multi Line assignment
print("*"*50)
print("Results of Relational Operators")
print("*"*50)
print("\t\t {} > {} = {}".format(a,b,a>b))
print("\t\t {} < {} = {}".format(a,b,a<b))
print("\t\t {} == {}={}".format(a,b,a==b))
print("\t\t {} != {} = {}".format(a,b,a!=b))
print("\t\t {} >= {} = {}".format(a,b,a>=b))
print("\t\t {} <={} = {}".format(a,b,a<=b))
print("*"*50)